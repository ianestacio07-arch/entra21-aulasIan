package colaboracao;

public class OlaMundoComColaboracao {
    public static void main(String[] args) {
        System.out.println("Olá mundo com colaboração!");
        System.out.println("Estou no repositorio do IAN!");
        System.out.println("Entrei no Ian!");

        for (int i = 0; i < 100; i++) {
            System.out.println("Qualquer coisa");
        }
        for (int i = 100; i >= 1; i--) {
            System.out.println(i);
        }
    }
}